pub fn reverse_complement(dna: &[u8]) -> Vec<u8> {
    let mut result = dna.to_vec();
    let len = result.len();

    for (i, &base) in dna.iter().enumerate() {
        let complement = COMPLEMENT_TABLE[base as usize];
        result[len - i - 1] = complement;
    }
    result
}
